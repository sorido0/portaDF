


export const borderStyler = (lenguaje) => {

    if (lenguaje === "JavaScript") return { border: "1px #FFC300 solid" }
    if (lenguaje === "React") return { border: "1px #2599be solid" }
    if (lenguaje === "Nodejs") return { border: "1px #28B463 solid" }
    if (lenguaje === "MongoDB") return { border: "1px #0E6251 solid" }
    if (lenguaje === "HTML") return { border: "1px #FF8000 solid" }
    if (lenguaje === "CSS") return { border: "1px #2599be solid" }

}




